'''Dictionary in Python:-'''

# => it stores data in Key value pair format. 
# => it is also unordered , that means , it does not support indexing. 
# => The key must be unique. 
# => it is Mutable . but we can change only the value not Key . 

dictionary_1 = {
    "name" : "Gulam",
    "age" : 20,
    "city" : "Delhi",
}
print(dictionary_1)
print(dictionary_1["name"])
print(dictionary_1["age"])
print(dictionary_1["city"])

student = {
    "name" : "Rabbani",
    "age" : 20,
    "city" : "Delhi",
    "course" : "Python",
}
print(student)
print(student["name"])
print(student["age"])
print(student["city"])
print(student["course"])


