# # Membership Operator:-

'''in, not in'''

fruits= ["apple", "banana", "mango"]
print("apple" in fruits)

fruits= ["apple", "banana", "mango"]
print("grapes" not in fruits)

'''String examples'''

name = "gulam"
print("gulam" in name)
print("gulam" not in name)

name = "rabbani"
print("r" in name )
print("g" not in name )

'''Number list Example'''
numbers = [10, 20, 40, 30]
print("50" in numbers )
print("50" not in numbers )