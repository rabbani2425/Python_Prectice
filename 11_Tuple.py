'''Tuple in python:- it can store any type of data similar as list'''

# it is immutable => we cannot change the value of tuple once it is created.

tupleee = (22,33,44,55,"Rudra", 6.7)

print(tupleee)

# indexing => positive , NEgative indexing 

tupleee = (22,33,44,55,"Rudra", 6.7)

print(tupleee)

print(tupleee[2])
print(tupleee[4])


tupleee = (22,33,44,55,"Rudra", 6.7)

print(tupleee)

for i in tupleee:
    print(i)

